cars = ['bmw','audi','toyota','subaru']
print(cars)
cars.reverse() #reversing the actual order of the list, and not reverse sorting
print(cars) #printing list
print(sorted(cars)) #sorting only for printing
print(cars)
cars.sort() #sorting the actual list
print(cars)
cars.sort(reverse= True) #reverse sorting the list
print(cars)
print(len(cars)) #printing the length of the list
